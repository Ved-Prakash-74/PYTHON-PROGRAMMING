#for loop for string
s="python"
for i in s:
    print(i)

#for loop for a range
print("\n")
for i in range(1,5,2):
    print(i)

print("\n")
for i in range(1,5):
    print(i)

print("\n")
for i in range(5):
    print(i)

#for loop for list
print("\n")
a=[10,2.5,"abc"]
for i in a:
    print(i)

#for loop for tuple
print("\n")
a=(10,2.5,"abc")
for i in a:
    print(i)